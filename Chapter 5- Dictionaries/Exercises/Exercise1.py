#this is the dictionary, which lets us set a list of data with a key:value
Thancred_Waters = {
    "First_Name": "Thancred",
        
    "Last_Name": "Waters",
    
    "Age": 32,
            
    "City": "Limsa Lominsa",
    }


#This prints the entire dictionary
print (Thancred_Waters)


#These print only specific keys of a given dictionary
print (Thancred_Waters["First_Name"])

print (Thancred_Waters["Last_Name"])

print (Thancred_Waters["Age"])

print (Thancred_Waters["City"])
